#include "Point.h"
#include <iostream>



/////////////////////////////////////////////////////////////////////////////////////////
// CONSTRUCTOR IMPLEMENTATION                                                          //
/////////////////////////////////////////////////////////////////////////////////////////

Point::Point(double new_x = 0.0, double new_y = 0.0) {
    x = new_x;
    y = new_y;
}



/////////////////////////////////////////////////////////////////////////////////////////
// METHOD IMPLEMENTATIONS                                                              //
/////////////////////////////////////////////////////////////////////////////////////////

double Point::get_x() {
    return x;
};

double Point::get_y() {
    return y;
}

void Point::set_x(double new_x){
    x = new_x;
}

void Point::set_y(double new_y) {
    y = new_y;
}

void Point::print() {
    std::cout << "Point: (" << this->x << ", " << this->y << ").\n";
    //fprintf(stdout, "Point: (%f, %f).\n", this->x, this->y);
}
